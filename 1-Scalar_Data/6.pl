# Nondecimal Integer Literals
# Perl allows you to use nondecimal integer literals, such as binary, octal, 
# and hexadecimal numbers.

my $bin_num = 0b1010;   # a binary number (decimal 10)
my $oct_num = 0123;     # an octal number (decimal 83)
my $hex_num = 0x1A;     # a hexadecimal number (decimal 26)
