# Numeric Operators
# Perl provides a wide range of numeric operators for performing arithmetic
# operations on numbers.

my $a = 10;
my $b = 8;

my $sum = $a + $b;     # addition
my $diff = $a - $b;    # subtraction
my $prod = $a * $b;    # multiplication
my $quot = $a / $b;    # division
my $mod = $a % $b;     # modulus
my $exp = $a ** $b;    # exponentiation


print $sum."\n";
print $diff."\n";
print $prod."\n";
print $quot."\n";
print $mod."\n";
print $exp."\n";
